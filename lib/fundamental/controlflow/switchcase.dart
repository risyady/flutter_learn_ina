void main() {
  String color = "black";
  switch (color) {
    case "red":
    print("warna favorit kamu adalah red");
    break;
    case "blue":
    print("warna favorit kamu adalah blue");
    break;
    case "yellow":
    print("warna favorit kamu adalah yellow");
    break;
    case "green":
    print("warna favorit kamu adalah green");
    break;
    default:
    print("warna favorit kamu adalah $color");
  }
}